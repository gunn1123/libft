#include "libft.h"

int	ft_atoi(const char *str)
{	
	int	n;
	int	s;

	s = 1;
	n = 0;
	while ((*str >= '\t' && *str <= '\r') || *str == ' ')
		++str;
	if (*str == '+' || *str == '-')
	{
		if (*(str++) == '-')
			s *= -1;
		str++;
	}
	while (ft_isdigit(*str))
	{
		n *= 10;
		n += (*str++ - '0');
		str++;
	}
	return (n * s);
}
